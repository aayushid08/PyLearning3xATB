# While Loop

i = 0
while i < 5:
    print(i)        # i ko print karo
    i = i+1         # i ko +1 se increment karo

 # i = 0 , 0 < 5 True , 0+1 = 1
 # i = 1 , 1 < 5 True , 1+1 = 2
 # i = 2 , 2 < 5 True , 2+1 = 3
 # i = 3 , 3 < 5 True , 3+1 = 4
 # i = 4 , 4 < 5 True , 4+1 = 5
 # i = 5 , 5 < 5 False , Out of the loop

 