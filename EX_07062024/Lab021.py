name = "This is the big line"
print(len(name))
print(type(name))
print(name[2]) # this will give us the index value
print(name[2:5]) # this will give us the index value from 2 to 5
print(name[2:]) # this will give us the index value from 2 to end
print(name[:5]) # this will give us the index value from start to 5
print(name[-1]) # this will give us the last index value.
# -1 ka matlab hai start from the last index value. (e rahega -1 pe aur 20 pe bhi)
