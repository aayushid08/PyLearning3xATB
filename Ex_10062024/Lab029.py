# Arithmetic Operators
# +, -, *, /, %, //, **
a = 180
b = 90
print(a + b)
print(a - b)
print(a * b)
print(a / b)  # float
print(a // b)  # int. // will return the quotient
# print(a ** b)  # power of a to b
print(a % b)  # remainder of a/b

# Modulus - Operator -> Remainder
# 90 / 180 / 2    2 is quotient
#    / 180 /
#    / 0 /        0 is remainder

print("Mod is:", 87 % 4)

print(a ** b)  # power of a to b
r = pow(10, 2)
print(r)

print(10 // 2)  # returns the quotient
print(10 % 2)  # returns the remainder


















